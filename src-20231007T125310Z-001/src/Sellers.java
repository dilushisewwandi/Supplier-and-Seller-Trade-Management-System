 void saveAllData() {
    try {
        FileWriter filewriter = new FileWriter("supplierdata.txt"); 
        for(Seller seller : allsellers) {
            filewriter.write(seller.getId() + ";" + seller.getAge() + ";" + seller.getSupname() + ";" + seller.getGender() + ";" + seller.getAddress() + ";" + seller.getContactnum()+"\n");
             filewriter.write(seller + "\n");
        }
        
        filewriter.close();
    } catch(Exception e) {
        e.printStackTrace();
    }
}
   
public void deleteSeller(int id) {
    Iterator<Seller> iterator = allsellers.iterator();
    while (iterator.hasNext()) {
        Seller seller = iterator.next();
        if (seller.getId() == id) {
            iterator.remove();
            saveAllData(); // update the data file
            return; // exit the method after deleting the first matching supplier
        }
    }
    // if no supplier with the given ID was found
    JOptionPane.showMessageDialog(null, "No supplier with ID " + id + " found.");
} 
    
    
   boolean checkID() {
    Queue<Seller> sellersQueue = new LinkedList<>(allsellers);
    Seller seller;

    while ((seller = sellersQueue.poll()) != null) {
        if (sellerid.getText().equals(String.valueOf(seller.getId())) || 
            sellercontact.getText().equals(seller.getContactnum())) {
            return true;
        }
    }
    return false;
   
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        if(sellerid.getText() == null || sellerid.getText().equals("")) {
            return;
        }
        
        Seller foundSeller = searchseller(sellerid.getText());
        if(foundSeller != null) {
            sellerid.setText(foundSeller.getId() + "");
            sellername.setText(foundSeller.getSupname());
            sellerage.setText(foundSeller.getAge() + "");
            sellergender.setText(foundSeller.getGender());
            selleraddress.setText(foundSeller.getAddress());
            sellercontact.setText(foundSeller.getContactnum());
            
        } else {
            JOptionPane.showMessageDialog(null, "Seller not found.");
        }
    }
    
private Seller searchseller(String input) {
        Queue<Seller> queue = new LinkedList<>(allsellers);
        while(!queue.isEmpty()) {
            Seller seller = queue.remove();
            if(seller.getId() == Integer.parseInt(input) || seller.getContactnum().equals(input)) {
                return seller;
            }
        }
        return null;
    
    }//GEN-LAST:event_btnsearchActionPerformed

