      void readAllData() {
        try {
            File supFile = new File("sellerdata.txt"); 
            Scanner scanner = new Scanner(supFile);
         
            while(scanner.hasNextLine()) {
                String data = scanner.nextLine();
                String[] currentdata = data.split(";");
                Seller seller = new Seller();
                seller.setId(Integer.parseInt(currentdata[0]));
                seller.setAge(Integer.parseInt(currentdata[1]));
                seller.setSupname(currentdata[2]);
                seller.setGender(currentdata[3]);
                seller.setAddress(currentdata[4]);
                seller.setContactnum(currentdata[5]);
                allsellers.offer(seller); // add to queue
            }
         
            scanner.close();
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
      
      

    
    
    
    
